package com.company;

public class Calculator {

    private int height;
    private int width;
    private int area;

    public void setHeight (int objectHeight) {
        height = objectHeight;
    }

    public int getHeight() {
        return height;
    }

    public void setWidth (int objectWidth) {
        width = objectWidth;
    }

    public int getWidth() {
        return width;
    }

    public void setArea (int objectArea) {
        area = objectArea;
    }

    public int getArea() {
        return area;
    }


}
